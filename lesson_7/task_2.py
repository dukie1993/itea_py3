'''
Користувач вводить 2 рядки. з даних рядків, відфільтрувати тільки цифри, 
об’єднати їх у єдине число, і перевірити, чи числа рівні.
Тобто s1 = “ewr123”, s2 = “1sd23”; n1 = 123, n2 = 123. n1 рівне n2
'''

s1 = input("a: ")
s2 = input("b: ")
n1 = ""
n2 = ""
for i in s1:
    if i.isdigit():
        n1 = n1 + i

for i in s2:
    if i.isdigit():
        n2 = n2 + i

if n1 == n2:
    print("Рядки рівні")